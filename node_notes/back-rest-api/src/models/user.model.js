import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      trim: true,
    },
  },
  {
    timestamps: true,
  }
);

// exportamos como un modelo el userSchema, y lo llamamos haciendo referencia a la entidad User con el valor de userSchema
export default mongoose.model("User", userSchema); 
